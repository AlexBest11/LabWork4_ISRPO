def area(a, b):
    '''
    На вход принимает число a и число b, возвращает произведение a и b
    Пример:
        Ввод:
        a = 3
        b = 4
        Вывод:
        area = 12
    '''
    return a * b 

def perimeter(a, b):
    '''
    На вход принимает число a и число b, возвращает удвоенную сумму a и b
    Пример:
        Ввод:
        a = 3
        b = 4
        Вывод:
        perimeter = 14
    '''
    P = (a + b) * 2
    return P
