def area(a, h):
    '''
    На вход принимает число a и число h, возвращает произведение чисел a и h, делённое на 2
    Пример:
        Ввод:
        a = 2
        h = 2
        Вывод:
        area = 2
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    На вход принимает число a, число b и число c, возвращает сумму чисел a, b и c
    Пример:
        Ввод:
        a = 3
        b = 4
        c = 5
        Вывод:
        perimeter = 12
    '''
    return a + b + c
